# Descrição de uma pessoa aleatória

# Strings (str) -> Representam cadeias de caracteres
nome = "Fulano da Silva Ciclano"
# Inteiros (int) -> Representam numeros inteiros
idade = 35
# Float (float) -> Representam os numeros reais (com o ponto separador de decimal)
altura = 1.82
# Booleanos (bool) -> Representam valores lógicos (verdadeiro e falso)
casado = True
solteiro = False
# Nulos (None) -> Ausência de valor significativo
# Quando uma característica não se aplica, ou é opcional informar
bigode = None
cabelo = None

